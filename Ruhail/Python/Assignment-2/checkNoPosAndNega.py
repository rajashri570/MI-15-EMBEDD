
# Python Program to check number is positive, negative or zero

num = int(input("Enter any Number to check weather is positive and negative : "))

if(num > 0):
    print("Number is Positive") #if given number is greater to 0, called positive number

elif(num == 0):

    print("Number is Zero")     #if given number is Equal to 0 we say it is zero number

else:

    print("Number is Negative") #both condition is not satisfed then it will....