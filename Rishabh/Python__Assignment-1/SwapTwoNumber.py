

            # Prog 4 : Python Program to swap two variables


num1 = float(input("Enter first number : "))
num2 = float(input("Enter first number : "))
print("Before swapping : First No = {0} and Second No = {1} ".format(num1, num2))
temp = num1
num1 = num2
num2 = temp
print("After swapping : First No = {0} and Second No = {1} ".format(num1, num2))

