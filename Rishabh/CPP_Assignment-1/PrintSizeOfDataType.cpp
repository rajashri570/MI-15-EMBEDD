
    // 1:- Write a program to print size of all datatypes in c++

#include <iostream>
using namespace std;

int main()
{
    cout<<"Size of char Data Type : "<<sizeof(char)<<"\n";
    cout<<"Size of short Data Type : "<<sizeof(short)<<"\n";
    cout<<"Size of int Data Type : "<<sizeof(int)<<"\n";
    cout<<"Size of float Data Type : "<<sizeof(float)<<"\n";
    cout<<"Size of double Data Type : "<<sizeof(double)<<"\n";
    cout<<"Size of long Data Type : "<<sizeof(long)<<"\n";
}